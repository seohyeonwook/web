// 비교연산자
console.log(10 == "10");    // 타입비교 x -> 타입이 존재하지 않기 때문에 true나온다
console.log(10 === "10");   // 타입비교 o -> 근데 ===세개쓰면 타입비교 해준다
console.log(10 != "10");
console.log(10 !== "10");
